/** Generate and verify the source-linked TypeScript UI symbol reference. */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import ts from "typescript";

const webRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = path.resolve(webRoot, "..");
const outputPath = path.join(repoRoot, "docs/generated/web-symbols.md");
const write = process.argv.includes("--write");

/** Return the first normalized sentence from a declaration's JSDoc. */
function summaryFor(node) {
  const docs = ts.getJSDocCommentsAndTags(node).filter((item) => ts.isJSDoc(item));
  const value = docs
    .map((doc) => (typeof doc.comment === "string" ? doc.comment : ""))
    .filter(Boolean)
    .join(" ")
    .replace(/\s+/g, " ")
    .trim();
  if (!value) return "";
  const end = value.indexOf(". ");
  return (end >= 0 ? value.slice(0, end + 1) : value).replaceAll("|", "\\|");
}

/** Collect documented top-level UI types, functions, and constants. */
function collectDeclarations() {
  const declarations = [];
  const undocumented = [];
  const files = fs
    .readdirSync(path.join(webRoot, "src"))
    .filter((name) => /\.(ts|tsx)$/.test(name))
    .sort();

  for (const name of files) {
    const absolute = path.join(webRoot, "src", name);
    const source = ts.createSourceFile(
      absolute,
      fs.readFileSync(absolute, "utf8"),
      ts.ScriptTarget.Latest,
      true,
      name.endsWith("x") ? ts.ScriptKind.TSX : ts.ScriptKind.TS,
    );
    for (const statement of source.statements) {
      const rows = [];
      if (ts.isTypeAliasDeclaration(statement) || ts.isInterfaceDeclaration(statement)) {
        rows.push({ kind: "type", symbol: statement.name.text, node: statement });
      } else if (ts.isFunctionDeclaration(statement) && statement.name) {
        rows.push({ kind: "function", symbol: statement.name.text, node: statement });
      } else if (ts.isVariableStatement(statement)) {
        for (const declaration of statement.declarationList.declarations) {
          if (ts.isIdentifier(declaration.name)) {
            rows.push({ kind: "constant", symbol: declaration.name.text, node: statement });
          }
        }
      }
      for (const row of rows) {
        const line = source.getLineAndCharacterOfPosition(row.node.getStart(source)).line + 1;
        const summary = summaryFor(row.node);
        const label = `web/src/${name}:${line} ${row.symbol}`;
        if (!summary) {
          undocumented.push(label);
        } else {
          declarations.push({ ...row, source: `web/src/${name}`, line, summary });
        }
      }
    }
  }
  return { declarations, undocumented };
}

/** Render a deterministic Markdown table from collected UI declarations. */
function render(declarations) {
  const lines = [
    "# Generated Web Symbol Reference",
    "",
    "Generated by `npm --prefix web run docs:write`. Do not edit this file by hand. " +
      "Every top-level type, function, and constant under `web/src/` must have JSDoc.",
    "",
    `Declarations: **${declarations.length}**`,
    "",
    "| Kind | Symbol | Source | Contract summary |",
    "| --- | --- | --- | --- |",
  ];
  for (const item of declarations) {
    lines.push(`| ${item.kind} | \`${item.symbol}\` | [\`${item.source}:${item.line}\`](../../${item.source}#L${item.line}) | ${item.summary} |`);
  }
  return `${lines.join("\n")}\n`;
}

const { declarations, undocumented } = collectDeclarations();
if (undocumented.length) {
  console.error(`Undocumented web declarations:\n${undocumented.join("\n")}`);
  process.exit(1);
}
const generated = render(declarations);
if (write) {
  fs.mkdirSync(path.dirname(outputPath), { recursive: true });
  fs.writeFileSync(outputPath, generated);
} else if (!fs.existsSync(outputPath) || fs.readFileSync(outputPath, "utf8") !== generated) {
  console.error(`${path.relative(repoRoot, outputPath)} is stale; run npm --prefix web run docs:write`);
  process.exit(1);
}
